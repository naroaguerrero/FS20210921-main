/* EJERCICIO 2 */
function ejercicio2(num) {
  let rnd = Math.floor(Math.random() * 100) + 1;
  console.log(
    "El número generado es: " + rnd + " y el número introducido es: " + num
  );
  if (rnd == num) {
    console.log("Los números son iguales");
  } else if (rnd > num) {
    console.log("El número generado aleatoriamente es mayor");
  } else if (rnd < num) {
    console.log("El número generado aleatoriamente es menor");
  }
}
